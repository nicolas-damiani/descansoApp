    private boolean verificarCampos (){
        errores = new ArrayList<>();
        boolean sinErrores = true;
        
        if(tFNombre.getText().isEmpty()){
            sinErrores = false;
           errores.add("Nombre");
        }if(tFDetalles.getText().isEmpty()){
            sinErrores = false;
           errores.add("Detalles");
        }if(tFCategoria.getText().isEmpty()){
            sinErrores = false;
           errores.add("Categoria");
        }if(tFHorario.getText().isEmpty()){
            sinErrores = false;
           errores.add("Horario");
        }if(tFUbicacion.getText().isEmpty()){
            sinErrores = false;
           errores.add("Ubicacion");
        }if(tFTelefono.getText().isEmpty()){
            sinErrores = false;
           errores.add("Telefono");
        }if(tFWebLink.getText().isEmpty()){
            sinErrores = false;
           errores.add("Web Link");
        } if(tFPrecio.getText().isEmpty()){
            sinErrores = false;
           errores.add("Precio");
        } if(cBCiudad.getSelectedItem().toString().compareTo("Seleccionar Ciudad")==0){
            sinErrores = false;
            errores.add("Ciudad");
        } if(cBTipoCA.getSelectedItem().toString().compareTo("Tipo de Comercio/Actividad")==0){
            sinErrores = false;
            errores.add("ComercioActividad");
        }
        return sinErrores;
    }

